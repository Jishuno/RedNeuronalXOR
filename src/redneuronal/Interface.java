    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Input1 = Box1.getSelectedItem().toString();
        Input2 = Box2.getSelectedItem().toString();
        double In1 = Double.parseDouble(Input1);
        double In2 = Double.parseDouble(Input2);

        neu = new NeuroRedXor(In1, In2);
        try {
            this.Matrix = neu.CalcXor();
        } catch (IOException ex) {
            Logger.getLogger(Interface.class.getName()).log(Level.SEVERE, null, ex);
        }

        jTextArea1.setText(
                  Matrix[0][0] + "     xor     " + Matrix[0][1] + "   =   " + Matrix[0][2] + "\n"
                + Matrix[1][0] + "     xor     " + Matrix[1][1] + "   =   " + Matrix[1][2] + "\n"
                + Matrix[2][0] + "     xor     " + Matrix[2][1] + "   =   " + Matrix[2][2] + "\n"
                + Matrix[3][0] + "     xor     " + Matrix[3][1] + "   =   " + Matrix[3][2]);

        try {
            neu.CalcularRed(In1, In2);
        } catch (IOException ex) {
            Logger.getLogger(Interface.class.getName()).log(Level.SEVERE, null, ex);
        }
        Actual = TextBox.getText();

        TextBox.setText(Actual + "\n" + neu.getResult());
    }//GEN-LAST:event_jButton1ActionPerformed

